# 20, 21, 22 ... 30 (all the numbers between 20 and 30, inclusive)
range(20,31)

# Even numbers: 2, 4, 6, 8, 10
range(2,12,2)

# Years ending in zero between 1900 and 2000 (inclusive)
range(1900, 2001,10)

